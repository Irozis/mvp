const ALIASES: Partial<Record<FormatKey, keyof typeof BASE_RULESETS>> = {}

function getFamilyFallback(format: FormatDefinition): FormatFamily {
  if (format.category === 'presentation') return 'presentation'
  if (format.key === 'print-billboard') return 'billboard'
  if (format.key === 'print-flyer-a5') return 'flyer'
  if (format.key === 'print-poster-a4') return 'poster'
  if (format.key === 'display-mpu' || format.key === 'display-large-rect') return 'display-rectangle'
  if (format.key === 'display-skyscraper' || format.key === 'display-halfpage') return 'display-skyscraper'
  if (format.key === 'display-leaderboard' || format.key === 'display-billboard') return 'display-leaderboard'
  if (format.family === 'square') return 'square'
  if (format.family === 'portrait' || format.family === 'skyscraper') return 'portrait'
  if (format.family === 'landscape') return 'landscape'
  if (format.family === 'wide') return 'billboard'
  return 'landscape'
}

export const FORMAT_RULESETS = Object.values(BASE_RULESETS)

export const FORMAT_RULESET_MAP: Record<string, FormatRuleSet> = {
  ...BASE_RULESETS,
}

for (const [targetKey, sourceKey] of Object.entries(ALIASES)) {
  const targetFormat = FORMAT_MAP[targetKey as FormatKey]
  FORMAT_RULESET_MAP[targetKey] = scaleRule(BASE_RULESETS[sourceKey], targetFormat)
}

export function getClosestFormatRuleSet(format: FormatDefinition): FormatRuleSet {
  const family = getFamilyFallback(format)
  const candidates = FORMAT_RULESETS.filter((rule) => rule.family === family)
  const targetRatio = format.width / format.height
  const closest = candidates.sort((left, right) => {
    const leftDiff = Math.abs(left.width / left.height - targetRatio) + Math.abs(left.width - format.width) / Math.max(format.width, 1)
    const rightDiff = Math.abs(right.width / right.height - targetRatio) + Math.abs(right.width - format.width) / Math.max(format.width, 1)
    return leftDiff - rightDiff
  })[0]
  return closest || BASE_RULESETS['social-square']
}

export function getFormatRuleSet(format: FormatDefinition): FormatRuleSet {
  return FORMAT_RULESET_MAP[format.key] || scaleRule(getClosestFormatRuleSet(format), format)
}
