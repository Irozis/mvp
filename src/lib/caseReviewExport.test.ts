        inputPath: 'C:/input.png',
        caseFolderPath: 'C:/case',
        status: 'success',
        baselineWon: true,
        winnerAccepted: false,
        winnerKind: 'local-structural-repair',
        winnerStrategyLabel: 'current',
        baselineAggregate: 44,
        winnerAggregate: 44,
        delta: 0,
        baselineConfidence: 50,
        winnerConfidence: 50,
        winnerConfidenceDelta: 0,
        reviewPriority: 'urgent-review',
        whyReview: 'Baseline retained despite available repair candidates',
        shortSummary: 'Baseline retained.',
        topTags: ['structural-drift'],
        topRejectionReasons: ['role-placement-out-of-zone'],
        dominantTags: ['structural-drift'],
        dominantPenalties: ['weak-balance'],
        issueCodes: ['structural-major-overlap'],
        structuralStatus: 'invalid',
        verdict: 'Poor',
        score: 0,
        bestRejectedCandidateKind: 'guided-regeneration-repair',
        bestRejectedCandidateId: 'candidate-1',
        bestRejectedCandidateStrategy: 'guided-a',
        bestRejectedCandidateAggregate: 49,
        bestRejectedCandidateDelta: 5,
        bestRejectedCandidateConfidence: 58,
        bestRejectedCandidateRejectionReasons: ['role-placement-out-of-zone'],
        bestRejectedCandidatePrimaryBlocker: 'role-placement-out-of-zone',
        bestRejectedCandidateOnlyBlockedByOneGate: true,
        bestRejectedCandidateWouldBeatBaseline: true,
        bestRejectedCandidateWouldImproveConfidence: true,
        hasPositiveRejectedCandidate: true,
