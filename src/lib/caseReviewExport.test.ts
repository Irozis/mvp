async function createCaseArtifacts(
  root: string,
  input: {
    category: string
    format: 'square' | 'landscape' | 'portrait'
    id: string
    family?: string
    baselineWon?: boolean
    winnerAggregate?: number
    reviewPriority?: 'urgent-review' | 'high-review' | 'medium-review' | 'low-review'
    includeCalibration?: boolean
  }
) {
  const caseDir = path.join(root, '_cases', input.category, input.format, input.id)
  await mkdir(caseDir, { recursive: true })
  const notes: CalibrationCaseNotes = {
    id: input.id,
    family: input.family || 'display',
    format: input.format,
    source: 'manual',
    category: input.category,
    expectedProblems: ['cramped-text'],
    expectedBehavior: ['preserve-layout-structure'],
    tags: ['test-case'],
  }
  await writeFile(path.join(caseDir, 'input.png'), PNG_1X1)
  await writeJson(path.join(caseDir, 'notes.json'), notes)

  const baselineWon = input.baselineWon ?? true
  const baselineAggregate = 44
  const winnerAggregate = input.winnerAggregate ?? (baselineWon ? 44 : 50)
  const delta = winnerAggregate - baselineAggregate
  const baselineCandidate = createCandidate({
    candidateId: 'baseline',
    candidateKind: 'local-structural-repair',
    strategyLabel: 'current',
    aggregateScore: baselineAggregate,
    aggregateDelta: 0,
    accepted: baselineWon,
    confidence: 50,
  })
  const bestRejected = createCandidate({
    candidateId: 'candidate-a',
    candidateKind: 'guided-regeneration-repair',
    strategyLabel: 'guided-a',
    aggregateScore: 49,
    aggregateDelta: 5,
    accepted: false,
    rejectionReasons: ['role-placement-out-of-zone'],
    confidence: 58,
  })
  const secondRejected = createCandidate({
    candidateId: 'candidate-b',
    candidateKind: 'image-balance-repair',
    strategyLabel: 'image-balance',
    aggregateScore: 47,
    aggregateDelta: 3,
    accepted: false,
    rejectionReasons: ['legacy-safety-rejection', 'no-net-gain'],
    confidence: 55,
  })
  const winnerCandidate = baselineWon
    ? baselineCandidate
    : createCandidate({
        candidateId: 'winner',
        candidateKind: 'guided-regeneration-repair',
        strategyLabel: 'winner',
        aggregateScore: winnerAggregate,
        aggregateDelta: delta,
        accepted: true,
        confidence: 60,
      })

  await writeJson(path.join(caseDir, 'baseline.json'), {
    structuralStatus: 'invalid',
    score: 0,
    verdict: 'Poor',
    issueCodes: ['structural-major-overlap'],
    confidence: {
      effectiveScore: 50,
      needsHumanAttention: true,
    },
    aggregateScore: baselineAggregate,
  })
  await writeJson(path.join(caseDir, 'winner.json'), {
    winnerCandidateKind: baselineWon ? 'local-structural-repair' : 'guided-regeneration-repair',
    winnerStrategyLabel: baselineWon ? 'current' : 'winner',
    structuralStatus: baselineWon ? 'invalid' : 'valid',
    score: baselineWon ? 0 : 60,
    verdict: baselineWon ? 'Poor' : 'Acceptable',
    issueCodes: baselineWon ? ['structural-major-overlap'] : ['spacing-warning'],
    confidence: {
      effectiveScore: baselineWon ? 50 : 60,
      needsHumanAttention: baselineWon,
    },
    aggregateScore: winnerAggregate,
  })
  await writeJson(path.join(caseDir, 'telemetry.json'), {
    telemetry: {
      formatKey: input.format === 'square' ? 'social-square' : 'display-mpu',
      formatFamily: input.format,
      aspectMode: input.format,
      baselineCandidateId: 'baseline',
      baselineAggregateScore: baselineAggregate,
      baselineConfidence: {
        effectiveScore: 50,
        disagreement: 0,
        needsHumanAttention: true,
      },
      winnerCandidateId: baselineWon ? 'baseline' : 'winner',
      winnerCandidateKind: baselineWon ? 'local-structural-repair' : 'guided-regeneration-repair',
      winnerStrategyLabel: baselineWon ? 'current' : 'winner',
      winnerAggregateScore: winnerAggregate,
      winnerDeltaVsBaseline: delta,
      winnerConfidence: {
        effectiveScore: baselineWon ? 50 : 60,
        disagreement: 0,
        needsHumanAttention: baselineWon,
      },
      winnerConfidenceDelta: baselineWon ? 0 : 10,
      baselineWon,
      candidateBudgetUsage: {
        configured: 8,
        nonBaselineEvaluated: 2,
        totalEvaluated: 3,
        remaining: 5,
        combinationConfigured: 2,
        combinationEvaluated: 1,
      },
      dominantTags: ['insufficient-breathing-room'],
      dominantPenalties: ['weak-balance'],
      candidates: [
        {
          candidateId: baselineCandidate.candidateId,
          strategyLabel: baselineCandidate.strategyLabel,
          candidateKind: baselineCandidate.candidateKind,
          structuralStatus: baselineCandidate.structuralStatus,
          aggregateScore: baselineCandidate.aggregateScore,
          aggregateDelta: baselineCandidate.aggregateDelta,
          accepted: baselineCandidate.accepted,
          rejectionReasons: baselineCandidate.rejectionReasons,
          gateOutcomes: baselineCandidate.gateOutcomes,
          summaryTags: baselineCandidate.summaryTags,
          penaltyTags: baselineCandidate.penaltyTags,
          confidence: baselineCandidate.confidence,
          confidenceDelta: baselineCandidate.confidenceDelta,
        },
      ],
    },
    candidateComparisons: baselineWon
      ? [baselineCandidate, bestRejected, secondRejected]
      : [baselineCandidate, winnerCandidate, secondRejected],
  })

  if (input.includeCalibration !== false) {
    await writeJson(path.join(caseDir, 'calibration.json'), {
      formatKey: input.format === 'square' ? 'social-square' : 'display-mpu',
      formatFamily: input.format,
      aspectMode: input.format,
      thresholds: {
        minAggregateGain: 0.75,
        maxConfidenceRegression: 8,
        maxSpacingViolationIncrease: 1,
        maxSpacingGapDeficitIncrease: 1.2,
        allowRolePlacement: false,
      },
      objectiveProfile: {
        weights: {
          structuralValidity: 0.36,
          perceptualQuality: 0.24,
          commercialStrength: 0.14,
          familyFidelity: 0.16,
          sideEffectCost: 0.1,
        },
        perceptualWeights: {
          cluster: 0.22,
          cta: 0.18,
          balance: 0.22,
          deadSpaceQuality: 0.18,
          readingFlow: 0.1,
          overall: 0.1,
        },
        sideEffectWeights: {
          disagreement: 1.5,
          deadSpace: 0.6,
          unresolved: 6,
          high: 12,
          critical: 18,
          geometry: 0.45,
          clusterRegression: 0.45,
          balanceRegression: 0.55,
          readingFlowRegression: 0.15,
          ctaDisconnectRegression: 0.18,
          verticalSeparationRegression: 0.15,
          inactiveSideRegression: 0.25,
        },
      },
      baseline: baselineCandidate,
      winner: baselineWon ? baselineCandidate : winnerCandidate,
      candidateComparisons: baselineWon
        ? [baselineCandidate, bestRejected, secondRejected]
        : [baselineCandidate, winnerCandidate, secondRejected],
    })
  }

  await writeJson(path.join(caseDir, 'report.json'), {
    caseId: input.id,
    category: input.category,
    format: input.format,
    family: input.family || 'display',
    formatKey: input.format === 'square' ? 'social-square' : 'display-mpu',
    status: 'success',
    baselineAggregate,
    winnerAggregate,
    aggregateDelta: delta,
    baselineWon,
    winnerAccepted: !baselineWon,
    winnerCandidateKind: baselineWon ? 'local-structural-repair' : 'guided-regeneration-repair',
    winnerStrategyLabel: baselineWon ? 'current' : 'winner',
    baselineConfidence: 50,
    winnerConfidence: baselineWon ? 50 : 60,
    winnerConfidenceDelta: baselineWon ? 0 : 10,
    topTags: ['insufficient-breathing-room'],
    topRejectionReasons: baselineWon
      ? ['role-placement-out-of-zone', 'legacy-safety-rejection']
      : ['legacy-safety-rejection'],
    reviewPriority: input.reviewPriority || (baselineWon ? 'urgent-review' : 'medium-review'),
    whyReview: baselineWon ? 'Baseline retained despite available repair candidates' : 'Candidate improved aggregate score',
    shortSummary: baselineWon
      ? 'Baseline retained; a stronger candidate was blocked by role placement.'
      : 'A candidate won with a stronger aggregate score.',
    humanVerdictPresent: false,
    machineHumanAgreement: null,
    agreementType: null,
    artifactPaths: {
      input: 'input.png',
      notes: 'notes.json',
      baseline: 'baseline.json',
      winner: 'winner.json',
      telemetry: 'telemetry.json',
      calibration: input.includeCalibration === false ? undefined : 'calibration.json',
      report: 'report.json',
      previewBaseline: 'preview-baseline.png',
      previewWinner: 'preview-winner.png',
    },
  })

  return caseDir
}

